'use strict';
/**
 * 本命盤計算：整合曆法、安星、四化、神煞與大限小限。
 */
const 曆 = require('./calendar');
const C = require('./constants');
const S = require('./stars');
const { 取亮度 } = require('./brightness');

const { 天干, 地支 } = 曆;
const { 支轉宮, 宮轉支, 宮 } = S;

/**
 * 正規化輸入並推導生辰資訊。
 * @param {object} 輸入
 *   - 曆別: 'solar'（國曆，預設）或 'lunar'（農曆）
 *   - 年, 月, 日: 數字
 *   - 閏月: boolean（僅農曆輸入時有意義）
 *   - 時辰: 0–12（12 = 晚子時 23:00–00:00）
 *   - 性別: '男' 或 '女'
 *   - 閏月過半進位: boolean，預設 true（閏月十六以後依下月安星）
 */
function 解析生辰(輸入) {
  const 曆別 = 輸入.曆別 || 'solar';
  const 時辰 = Number(輸入.時辰);
  const 性別 = 輸入.性別;
  if (!(時辰 >= 0 && 時辰 <= 12)) throw new RangeError('時辰須為 0–12（12 = 晚子時）');
  if (性別 !== '男' && 性別 !== '女') throw new RangeError("性別須為 '男' 或 '女'");

  let 國曆, 農曆;
  if (曆別 === 'lunar') {
    農曆 = { 年: 輸入.年, 月: 輸入.月, 日: 輸入.日, 閏: !!輸入.閏月 };
    國曆 = 曆.農曆轉國曆(農曆.年, 農曆.月, 農曆.日, 農曆.閏);
  } else {
    國曆 = { 年: 輸入.年, 月: 輸入.月, 日: 輸入.日 };
    農曆 = 曆.國曆轉農曆(國曆.年, 國曆.月, 國曆.日);
  }

  // 晚子時（23:00–00:00）依「翌日子時」法：
  // 日柱採翌日干支、日系安星之日數加一；月份與年份不變，時支為子。
  let 日柱國曆 = 國曆;
  let 安星日 = 農曆.日;
  let 紫微日 = 農曆.日;
  if (時辰 === 12) {
    const d = new Date(Date.UTC(國曆.年, 國曆.月 - 1, 國曆.日) + 86400000);
    日柱國曆 = { 年: d.getUTCFullYear(), 月: d.getUTCMonth() + 1, 日: d.getUTCDate() };
    安星日 = 農曆.日 + 1;
    // 起紫微星之日數若超過當月天數則回繞至初一（日系雜曜不回繞）
    const 該月天數 = 農曆.閏 ? 曆.閏月天數(農曆.年) : 曆.月天數(農曆.年, 農曆.月);
    紫微日 = 安星日 > 該月天數 ? 安星日 - 該月天數 : 安星日;
  }
  const 時支 = 時辰 % 12;

  // 安星月：閏月過半（十六起）依下月安星；晚子時不適用此進位
  const 過半進位 = 輸入.閏月過半進位 !== false;
  let 安星月 = 農曆.月;
  if (農曆.閏 && 過半進位 && 農曆.日 > 15 && 時辰 !== 12) 安星月 += 1;
  if (安星月 > 12) 安星月 -= 12;

  // 月柱：一律採閏月過半進位（與安星月分開計，無晚子時例外）
  let 月柱月 = 農曆.月;
  if (農曆.閏 && 農曆.日 > 15) 月柱月 += 1;
  if (月柱月 > 12) 月柱月 -= 12;

  const 年柱 = 曆.年干支(農曆.年);
  const 月柱 = 曆.月干支(年柱.干, 月柱月);
  const 日柱序 = 曆.日干支序(日柱國曆.年, 日柱國曆.月, 日柱國曆.日);
  const 時柱 = 曆.時干支(日柱序 % 10, 時支);

  return {
    性別, 時辰, 時支, 國曆, 農曆, 安星月, 安星日, 紫微日,
    年柱, 月柱, 日柱: { 干: 日柱序 % 10, 支: 日柱序 % 12 }, 時柱,
  };
}

/** 西洋星座 */
function 星座(月, 日) {
  const n = 月 * 100 + 日;
  for (const [界, 名] of C.星座表) if (n <= 界) return 名;
  return '摩羯座';
}

/**
 * 排本命盤
 * @returns 完整命盤物件（十二宮、星曜、四化、神煞、大限小限）
 */
function 排盤(輸入) {
  const 生 = 解析生辰(輸入);
  const { 年柱, 安星月, 安星日, 時支, 性別 } = 生;

  // 命宮、身宮：寅宮起正月順數至生月，再起子時逆（命）／順（身）數至生時
  const 命宮 = 宮(安星月 - 1 - 時支);
  const 身宮 = 宮(安星月 - 1 + 時支);

  // 宮干（五虎遁，全盤十二宮）
  const 寅宮干 = ((年柱.干 % 5) * 2 + 2) % 10;

  // 五行局：命宮干支納音
  const 命宮干 = (寅宮干 + 命宮) % 10;
  const 命宮支 = 宮轉支(命宮);
  let 甲子序 = 0;
  for (let n = 0; n < 60; n++) if (n % 10 === 命宮干 && n % 12 === 命宮支) { 甲子序 = n; break; }
  const 五行 = C.納音五行序[Math.floor(甲子序 / 2)];
  const 局數 = C.五行局數[五行];

  // 安星
  const 紫微 = S.紫微宮位(局數, 生.紫微日);
  const 主星盤 = S.安十四主星(紫微);
  const 輔星盤 = S.安輔星({ 年干: 年柱.干, 年支: 年柱.支, 月: 安星月, 日: 安星日, 時支 });
  const 雜曜盤 = S.安雜曜({ 年干: 年柱.干, 年支: 年柱.支, 月: 安星月, 日: 安星日, 時支, 命宮, 身宮 });

  // 生年四化
  const 四化 = {};
  C.四化表[年柱.干].forEach((星, i) => { 四化[星] = C.四化名[i]; });

  // 大限：起運歲數＝局數，陽男陰女順行、陰男陽女逆行
  const 陽年 = 年柱.干 % 2 === 0;
  const 順行 = (陽年 && 性別 === '男') || (!陽年 && 性別 === '女');
  const 方向 = 順行 ? 1 : -1;

  // 博士十二神（自祿存起，順逆同大限）、長生十二神（依五行局，順逆同大限）
  const 祿存宮 = 支轉宮(C.祿存表[年柱.干]);
  const 長生宮 = 支轉宮(C.長生起點[五行]);
  // 將前十二神（將星起三合旺位順行）、歲前十二神（歲建起年支順行）
  const 將星宮 = 支轉宮([0, 9, 6, 3][年柱.支 % 4]); // 申子辰在子、巳酉丑在酉、寅午戌在午、亥卯未在卯
  const 歲建宮 = 支轉宮(年柱.支);

  // 小限：依年支三合起宮，男順女逆，一歲起
  const 小限起 = 支轉宮(C.小限起點[年柱.支 % 4]);
  const 小限方向 = 性別 === '男' ? 1 : -1;

  const 星物件 = (名, 支索引, 範圍 = 'origin') => ({
    名稱: 名,
    亮度: 取亮度(名, 支索引),
    四化: 四化[名] || '',
  });

  const 宮位 = [];
  for (let i = 0; i < 12; i++) {
    const 支索引 = 宮轉支(i);
    const 宮序 = 宮(命宮 - i); // 自命宮逆排十二宮名
    const 大限序 = 宮((i - 命宮) * 方向); // 第幾個大限（0 起）
    const 小限歲 = [];
    for (let a = 1; 小限歲.length < 10; a++) {
      if (宮(小限起 + 小限方向 * (a - 1)) === i) 小限歲.push(a);
    }
    宮位.push({
      宮位索引: i,
      宮名: C.宮名序[宮序],
      天干: 天干[(寅宮干 + i) % 10],
      地支: 地支[支索引],
      是身宮: i === 身宮,
      是來因宮: i < 10 && (寅宮干 + i) % 10 === 年柱.干,
      主星: 主星盤[i].map((名) => 星物件(名, 支索引)),
      輔星: 輔星盤[i].map((名) => 星物件(名, 支索引)),
      雜曜: 雜曜盤[i].map((名) => ({ 名稱: 名, 四化: 四化[名] || '' })),
      長生十二神: C.長生十二神[宮((i - 長生宮) * 方向)],
      博士十二神: C.博士十二神[宮((i - 祿存宮) * 方向)],
      將前十二神: C.將前十二神[宮(i - 將星宮)],
      歲前十二神: C.歲前十二神[宮(i - 歲建宮)],
      大限: {
        起: 局數 + 大限序 * 10,
        迄: 局數 + 大限序 * 10 + 9,
        天干: 天干[(寅宮干 + i) % 10],
        地支: 地支[支索引],
      },
      小限歲數: 小限歲,
    });
  }

  return {
    性別,
    國曆: `${生.國曆.年}-${生.國曆.月}-${生.國曆.日}`,
    農曆: 曆.農曆中文(生.農曆.年, 生.農曆.月, 生.農曆.日, 生.農曆.閏),
    四柱: `${天干[年柱.干]}${地支[年柱.支]} ${天干[生.月柱.干]}${地支[生.月柱.支]} ${天干[生.日柱.干]}${地支[生.日柱.支]} ${天干[生.時柱.干]}${地支[生.時柱.支]}`,
    時辰: 曆.時辰名[時支] + '時' + (生.時辰 === 12 ? '（晚子）' : ''),
    時段: 曆.時辰範圍[生.時辰],
    生肖: C.生肖表[年柱.支],
    星座: 星座(生.國曆.月, 生.國曆.日),
    命宮地支: 地支[宮轉支(命宮)],
    身宮地支: 地支[宮轉支(身宮)],
    命主: C.命主表[宮轉支(命宮)],
    身主: C.身主表[年柱.支 % 6],
    五行局: C.五行局名[五行],
    生年四化: Object.fromEntries(C.四化表[年柱.干].map((星, i) => [C.四化名[i], 星])),
    宮位,
    _內部: { 生, 命宮, 身宮, 局數, 方向, 寅宮干 },
  };
}

module.exports = { 排盤, 解析生辰, 星座 };
