'use strict';
/**
 * 安星訣：依生辰資訊計算各星曜所在宮位。
 * 宮位索引：寅 = 0 順行至丑 = 11；地支索引：子 = 0。
 */
const C = require('./constants');

/** 地支索引 → 宮位索引 */
const 支轉宮 = (支) => ((支 % 12) + 22) % 12;
/** 宮位索引 → 地支索引 */
const 宮轉支 = (宮) => ((宮 % 12) + 14) % 12;
/** 宮位正規化 */
const 宮 = (n) => ((n % 12) + 12) % 12;

/**
 * 紫微星宮位
 * 安星訣：以五行局數除生日，不足者借足倍數；商數自寅宮起算，
 * 借數為偶數則順行借數位、奇數則逆行借數位。
 * @param {number} 局數 2–6
 * @param {number} 生日 農曆日 1–30
 */
function 紫微宮位(局數, 生日) {
  const 商 = Math.ceil(生日 / 局數);
  const 借 = 商 * 局數 - 生日;
  const 位 = 借 % 2 === 0 ? 商 + 借 : 商 - 借;
  return 宮(位 - 1); // 寅宮起 1
}

/** 十四主星宮位：回傳 Map<宮位, 星名[]> 之普通物件（陣列長度 12） */
function 安十四主星(紫微) {
  const 盤 = Array.from({ length: 12 }, () => []);
  盤[紫微].push('紫微');
  for (const [星, 移] of Object.entries(C.紫微星系)) 盤[宮(紫微 + 移)].push(星);
  // 天府與紫微以寅申軸對稱
  const 天府 = 支轉宮(宮(4 - 宮轉支(紫微)));
  盤[天府].push('天府');
  for (const [星, 移] of Object.entries(C.天府星系)) 盤[宮(天府 + 移)].push(星);
  return 盤;
}

/**
 * 十四輔星（六吉、六煞、祿存、天馬）宮位
 * @param {object} ctx {年干, 年支, 月, 日, 時支}（月＝安星月、日＝安星日）
 */
function 安輔星(ctx) {
  const { 年干, 年支, 月, 時支 } = ctx;
  const 盤 = Array.from({ length: 12 }, () => []);
  const 放 = (星, 支索引) => 盤[支轉宮(支索引)].push(星);
  const 放宮 = (星, 宮索引) => 盤[宮(宮索引)].push(星);

  放('文昌', 10 - 時支);      // 戌宮起子時逆行
  放('文曲', 4 + 時支);       // 辰宮起子時順行
  放宮('左輔', 2 + (月 - 1)); // 辰宮起正月順行（辰=宮位2）
  放宮('右弼', 8 - (月 - 1)); // 戌宮起正月逆行
  放('天魁', C.天魁表[年干]);
  放('天鉞', C.天鉞表[年干]);
  const 祿存 = C.祿存表[年干];
  放('祿存', 祿存);
  放('擎羊', 祿存 + 1);
  放('陀羅', 祿存 - 1 + 12);
  放('火星', C.火星起點[年支 % 4] + 時支); // 依年支三合起點，順數至生時
  放('鈴星', C.鈴星起點[年支 % 4] + 時支);
  放('地劫', 11 + 時支);      // 亥宮起子時順行
  放('地空', 11 - 時支 + 12); // 亥宮起子時逆行
  放('天馬', C.天馬表[年支 % 4]);
  return 盤;
}

/**
 * 雜曜宮位
 * @param {object} ctx {年干, 年支, 月, 日, 時支, 命宮, 身宮}
 *   其中命宮/身宮為宮位索引；左輔右弼／昌曲位置由月、時推得（三台八座恩光天貴用）。
 */
function 安雜曜(ctx) {
  const { 年干, 年支, 月, 日, 時支, 命宮, 身宮 } = ctx;
  const 盤 = Array.from({ length: 12 }, () => []);
  const 放 = (星, 支索引) => 盤[支轉宮(宮(支索引))].push(星);
  const 放宮 = (星, 宮索引) => 盤[宮(宮索引)].push(星);

  // 年干系
  放('天官', C.天官表[年干]);
  放('天福', C.天福表[年干]);
  放('天廚', C.天廚表[年干]);
  放('截路', C.截路表[年干]);
  放('空亡', C.空亡表[年干]);

  // 旬空：生年干支所屬旬之空亡二支，取與年支同陰陽者
  const 甲子序 = (() => { for (let n = 0; n < 60; n++) if (n % 10 === 年干 && n % 12 === 年支) return n; })();
  const 旬 = Math.floor(甲子序 / 10);
  const 旬空陽支 = 宮(10 - 旬 * 2);
  放('旬空', 年支 % 2 === 0 ? 旬空陽支 : 旬空陽支 + 1);

  // 年支系
  const 鸞 = 宮(3 - 年支);
  放('紅鸞', 鸞);
  放('天喜', 鸞 + 6);
  放('龍池', 4 + 年支);   // 辰宮起子年順行
  放('鳳閣', 10 - 年支);  // 戌宮起子年逆行
  放('天哭', 6 - 年支);   // 午宮起子年逆行
  放('天虛', 6 + 年支);   // 午宮起子年順行
  放('天空', 年支 + 1);   // 年支後一位
  放('年解', 10 - 年支);  // 戌宮起子年逆行
  放('天德', 9 + 年支);   // 酉宮起子年順行
  放('月德', 5 + 年支);   // 巳宮起子年順行
  放('華蓋', C.華蓋表[年支 % 4]);
  放('咸池', C.咸池表[年支 % 4]);
  放('破碎', C.破碎表[年支 % 3]);
  // 孤辰寡宿：依年支四方（亥子丑→孤寅寡戌，順推）
  const 方 = Math.floor(((年支 + 1) % 12) / 3);
  放('孤辰', 2 + 方 * 3);
  放('寡宿', 10 + 方 * 3);
  // 蜚廉：子丑寅在申酉戌、卯辰巳在巳午未、午未申在寅卯辰、酉戌亥在亥子丑
  放('蜚廉', 年支 + (Math.floor(年支 / 3) % 2 === 0 ? 8 : 2));

  // 月系
  放('天刑', 9 + (月 - 1));  // 酉宮起正月順行
  放('天姚', 1 + (月 - 1));  // 丑宮起正月順行
  放('陰煞', 2 - 2 * (月 - 1) + 24); // 寅宮起正月，逆行隔位
  放('解神', 8 + Math.floor((月 - 1) / 2) * 2); // 申宮起正月，兩月一躍
  放('天巫', C.天巫表[(月 - 1) % 4]);
  放('天月', C.天月表[月 - 1]);

  // 時系
  放('台輔', 6 + 時支); // 午宮起子時順行
  放('封誥', 2 + 時支); // 寅宮起子時順行

  // 日系（以左輔右弼、文昌文曲為基準）
  const 左輔 = 宮(2 + (月 - 1));
  const 右弼 = 宮(8 - (月 - 1));
  const 文昌 = 支轉宮(宮(10 - 時支));
  const 文曲 = 支轉宮(宮(4 + 時支));
  放宮('三台', 左輔 + (日 - 1)); // 左輔起初一順數至生日
  放宮('八座', 右弼 - (日 - 1)); // 右弼起初一逆數至生日
  放宮('恩光', 文昌 + (日 - 1) - 1); // 文昌起初一順數至生日再退一步
  放宮('天貴', 文曲 + (日 - 1) - 1); // 文曲起初一順數至生日再退一步

  // 命身系
  放宮('天才', 命宮 + 年支); // 命宮起子年順數至本生年支
  放宮('天壽', 身宮 + 年支); // 身宮起子年順數至本生年支
  // 天傷居僕役宮、天使居疾厄宮
  放宮('天傷', 命宮 - 7);
  放宮('天使', 命宮 - 5);
  return 盤;
}

/**
 * 流曜（運限星）：依某層干支安十顆流曜（昌曲魁鉞鸞喜祿羊陀馬），
 * 並可附加年解（僅流年層）。
 * @param {number} 干 該層天干索引
 * @param {number} 支 該層地支索引
 * @param {string} 前綴 例：運、流、月、日、時
 * @param {boolean} 含年解 是否附加年解（流年層）
 */
function 安流曜(干, 支, 前綴, 含年解 = false) {
  const 盤 = Array.from({ length: 12 }, () => []);
  const 放 = (星, 支索引) => 盤[支轉宮(宮(支索引))].push(星);
  放(前綴 + '昌', C.流昌表[干]);
  放(前綴 + '曲', C.流曲表[干]);
  放(前綴 + '魁', C.天魁表[干]);
  放(前綴 + '鉞', C.天鉞表[干]);
  const 祿 = C.祿存表[干];
  放(前綴 + '祿', 祿);
  放(前綴 + '羊', 祿 + 1);
  放(前綴 + '陀', 祿 - 1 + 12);
  放(前綴 + '馬', C.天馬表[支 % 4]);
  const 鸞 = 宮(3 - 支);
  放(前綴 + '鸞', 鸞);
  放(前綴 + '喜', 鸞 + 6);
  if (含年解) 放('年解', 10 - 支);
  return 盤;
}

module.exports = { 支轉宮, 宮轉支, 宮, 紫微宮位, 安十四主星, 安輔星, 安雜曜, 安流曜 };
