'use strict';
/**
 * 文字命盤輸出：以樹狀純文字呈現完整命盤（適合直接餵給 LLM 解盤）。
 * 格式參考文墨天機之命盤文字導出樣式（僅含命盤資料，不含解盤提示詞）。
 */
const 曆 = require('./calendar');
const C = require('./constants');
const S = require('./stars');

const { 天干, 地支 } = 曆;
const { 支轉宮, 宮轉支, 宮 } = S;

/** 文字盤之宮名（僕役改稱交友，與通行命盤軟體一致） */
const 宮名顯示 = (名) => (名 === '僕役' ? '交友' : 名);

/** 星曜標記：名稱[亮度][生年X][↓離心][↑向心] */
function 星標(s) {
  let t = s.名稱;
  if (s.亮度) t += `[${s.亮度}]`;
  if (s.四化) t += `[生年${s.四化}]`;
  if (s.離心自化) t += `[↓${s.離心自化}]`;
  if (s.向心自化) t += `[↑${s.向心自化}]`;
  return t;
}

/**
 * 產生文字命盤
 * @param {object} 本命 排盤() 結果（含 _內部）
 * @param {object|null} 運限 排運限() 結果（可省略）
 */
function 文字盤(本命, 運限 = null) {
  const 生 = 本命._內部.生;
  const 行 = [];
  const p = (s) => 行.push(s);

  p('紫微斗數命盤');
  p('│');
  p('├符號定義');
  p('│ ├(↓:離心自化，本宮宮干使本宮星曜化出)');
  p('│ └(↑:向心自化，對宮宮干使本宮星曜化入)');
  p('│');
  p('├基本信息');
  p('│ │');
  p(`│ ├性別 : ${本命.性別}`);
  p(`│ ├國曆時間 : ${本命.國曆} ${本命.時辰}（${本命.時段}）`);
  p(`│ ├農曆時間 : ${本命.農曆} ${本命.時辰}`);
  p(`│ ├四柱 : ${本命.四柱}`);
  p(`│ ├五行局數 : ${本命.五行局}`);
  p(`│ ├生年四化 : ${['祿', '權', '科', '忌'].map((k) => `${本命.生年四化[k]}化${k}`).join('、')}`);
  p(`│ └身主:${本命.身主}; 命主:${本命.命主}; 子年斗君:${本命.子年斗君}; 身宮:${本命.身宮地支}`);
  p('│');
  p('├命盤十二宮');
  p('│ │');

  // 依地支序（子起）列十二宮
  const 年支 = 生.年柱.支;
  for (let 支 = 0; 支 < 12; 支++) {
    const i = 支轉宮(支);
    const q = 本命.宮位[i];
    const 標籤 = [];
    if (q.是身宮) 標籤.push('身宮');
    if (q.是來因宮) 標籤.push('來因');
    p(`│ ├${宮名顯示(q.宮名)}宮[${q.天干}${q.地支}]${標籤.map((t) => `[${t}]`).join('')}`);
    p(`│ │ ├主星 : ${q.主星.length ? q.主星.map(星標).join(',') : '無'}`);
    p(`│ │ ├輔星 : ${q.輔星.length ? q.輔星.map(星標).join(',') : '無'}`);
    p(`│ │ ├小星 : ${q.雜曜.length ? q.雜曜.map((s) => s.名稱).join(',') : '無'}`);
    p('│ │ ├神煞');
    p(`│ │ │ ├歲前星 : ${q.歲前十二神}`);
    p(`│ │ │ ├將前星 : ${q.將前十二神}`);
    p(`│ │ │ ├十二長生 : ${q.長生十二神}`);
    p(`│ │ │ └博士十二神 : ${q.博士十二神}`);
    p(`│ │ ├大限 : ${q.大限.起}~${q.大限.迄}虛歲`);
    p(`│ │ ├小限 : ${q.小限歲數.slice(0, 5).join(',')}虛歲`);
    const 流年歲 = ((支 - 年支 + 12) % 12) + 1;
    p(`│ │ ├流年 : ${[0, 1, 2, 3, 4].map((k) => 流年歲 + 12 * k).join(',')}虛歲`);
    if (運限) {
      const 疊 = [];
      const 層們 = [['大限', 運限.大限], ['小限', 運限.小限], ['流年', 運限.流年], ['流月', 運限.流月], ['流日', 運限.流日], ['流時', 運限.流時]];
      for (const [層名, 層] of 層們) {
        if (層.宮位索引 === i) 疊.push(`${層名}命宮`);
        else if (層.宮名) 疊.push(`${層名}${宮名顯示(層.宮名[i])}`);
      }
      p(`│ │ └限流疊宮 : ${疊.join('、') || '無'}`);
    } else {
      p('│ │ └限流疊宮 : 無（未指定運限目標日期）');
    }
    p('│ │');
  }

  // 十二大限與其流年
  p('├十二大限流年');
  p('│ │');
  const { 命宮, 局數, 方向, 寅宮干 } = 本命._內部;
  for (let k = 0; k < 12; k++) {
    const i = 宮(命宮 + 方向 * k);
    const q = 本命.宮位[i];
    const 起 = 局數 + k * 10;
    const 流年列 = [];
    for (let a = 起; a <= 起 + 9; a++) {
      const 西元 = 生.農曆.年 + a - 1;
      const 干支 = 曆.年干支(西元);
      流年列.push(`${a}歲${天干[干支.干]}${地支[干支.支]}(${西元})`);
    }
    const 前綴 = k === 11 ? '│ └' : '│ ├';
    p(`${前綴}第${k + 1}大限 ${起}~${起 + 9}虛歲 ${宮名顯示(q.宮名)}宮[${q.天干}${q.地支}]`);
    p(`│ ${k === 11 ? ' ' : '│'} └流年 : ${流年列.join('、')}`);
  }
  p('│');

  // 運限資訊（有指定目標日期時）
  if (運限) {
    p('├運限信息');
    p(`│ ├目標時間 : 國曆 ${運限.目標國曆}（${運限.目標農曆}），虛歲${運限.虛歲}`);
    const 層們 = [
      [`${運限.大限.名稱}(${運限.大限.起}~${運限.大限.迄}虛歲)`, 運限.大限],
      ['小限', 運限.小限], ['流年', 運限.流年], ['流月', 運限.流月], ['流日', 運限.流日], ['流時', 運限.流時],
    ];
    層們.forEach(([名, 層], idx) => {
      const 前綴 = idx === 層們.length - 1 ? '│ └' : '│ ├';
      const 宮支 = 地支[宮轉支(層.宮位索引)];
      const 四化 = 層.四化 ? ['祿', '權', '科', '忌'].map((k) => `${層.四化[k]}化${k}`).join('、') : '';
      p(`${前綴}${名} : ${層.天干}${層.地支}，命宮在${宮支}宮${四化 ? `，四化 : ${四化}` : ''}`);
    });
    p('│');
  }

  p('└[備註: 本命盤由 ziwei-chart 排出，宮位以地支標示；亮度為廟旺得利平不陷]');
  return 行.join('\n');
}

module.exports = { 文字盤 };
