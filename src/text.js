'use strict';
/**
 * 文字命盤輸出：以樹狀純文字呈現完整命盤（適合直接餵給 LLM 解盤）。
 * 格式參考文墨天機之命盤文字導出樣式（僅含命盤資料，不含解盤提示詞）。
 *
 * 可選輸出運限維度盤（大限／小限／流年／流月／流日／流時）：
 * 每一維度各自輸出完整十二宮（該層宮名重排、該層四化標記於星曜、該層流曜），
 * 讓 LLM 同時讀取多維度盤面，毋須自行推算疊宮。
 */
const 曆 = require('./calendar');
const C = require('./constants');
const S = require('./stars');

const { 天干, 地支 } = 曆;
const { 支轉宮, 宮轉支, 宮 } = S;

/** 文字盤之宮名（僕役改稱交友，與通行命盤軟體一致） */
const 宮名顯示 = (名) => (名 === '僕役' ? '交友' : 名);

/** 各運限層之標記前綴 */
const 層短 = { 大限: '運', 小限: '小', 流年: '年', 流月: '月', 流日: '日', 流時: '時' };
const 全部層 = ['大限', '小限', '流年', '流月', '流日', '流時'];

/** 星曜標記：名稱[亮度][生年X][↓離心][↑向心]，可附加運限層四化 */
function 星標(s, 層四化 = []) {
  let t = s.名稱;
  if (s.亮度) t += `[${s.亮度}]`;
  if (s.四化) t += `[生年${s.四化}]`;
  if (s.離心自化) t += `[↓${s.離心自化}]`;
  if (s.向心自化) t += `[↑${s.向心自化}]`;
  for (const [前綴, 四化] of 層四化) {
    C.四化名.forEach((名, k) => {
      if (四化[名] === s.名稱) t += `[${前綴}${名}]`;
    });
  }
  return t;
}

/** 單一運限維度之完整盤段 */
function 層盤段(p, 本命, 層名, 層) {
  const 短 = 層短[層名];
  const 命宮支 = 地支[宮轉支(層.宮位索引)];
  const 標題 = 層名 === '大限' && 層.名稱 === '童限' ? '童限' : 層名;
  p(`├${標題}盤［${層.天干}${層.地支}${層.起 !== undefined ? `，${層.起}~${層.迄}虛歲` : ''}，${標題}命宮在${命宮支}宮］`);
  if (層.四化) {
    p(`│ ├${標題}四化 : ${['祿', '權', '科', '忌'].map((k) => `${層.四化[k]}化${k}`).join('、')}`);
  }
  p(`│ ├十二宮（宮名以${標題}重排，括號內為本命宮名）`);
  for (let 支 = 0; 支 < 12; 支++) {
    const i = 支轉宮(支);
    const q = 本命.宮位[i];
    const 前 = 支 === 11 ? '│ │ └' : '│ │ ├';
    const 星曜 = [...q.主星, ...q.輔星].map((s) => 星標(s, [[短, 層.四化]])).join(',') || '無';
    const 流曜 = 層.流曜 ? (層.流曜[i].join(',') || '無') : null;
    p(`${前}${標題}${宮名顯示(層.宮名[i])}[${q.天干}${q.地支}]（本命${宮名顯示(q.宮名)}）｜星曜 : ${星曜}${流曜 !== null ? `｜${標題}流曜 : ${流曜}` : ''}`);
  }
  p('│');
}

/**
 * 產生文字命盤
 * @param {object} 本命 排盤() 結果（含 _內部）
 * @param {object|null} 運限 排運限() 結果（可省略）
 * @param {object} 選項 { 層: ['大限','小限','流年','流月','流日','流時'] }，預設全部
 */
function 文字盤(本命, 運限 = null, 選項 = {}) {
  const 生 = 本命._內部.生;
  // 選項.層 未提供＝全部維度；空陣列＝不輸出任何維度盤
  const 選層 = (選項.層 ? 選項.層 : 全部層).filter((x) => 全部層.includes(x));
  const 行 = [];
  const p = (s) => 行.push(s);

  p('紫微斗數命盤');
  p('│');
  p('├符號定義');
  p('│ ├(↓:離心自化，本宮宮干使本宮星曜化出)');
  p('│ ├(↑:向心自化，對宮宮干使本宮星曜化入)');
  p('│ └(運/小/年/月/日/時＋四化:該運限層之四化，如[年忌]為流年化忌)');
  p('│');
  p('├基本信息');
  p('│ │');
  p(`│ ├性別 : ${本命.性別}`);
  p(`│ ├國曆時間 : ${本命.國曆} ${本命.時辰}（${本命.時段}）`);
  p(`│ ├農曆時間 : ${本命.農曆} ${本命.時辰}`);
  p(`│ ├四柱 : ${本命.四柱}`);
  p(`│ ├五行局數 : ${本命.五行局}`);
  p(`│ ├生年四化 : ${['祿', '權', '科', '忌'].map((k) => `${本命.生年四化[k]}化${k}`).join('、')}`);
  p(`│ └身主:${本命.身主}; 命主:${本命.命主}; 子年斗君:${本命.子年斗君}; 身宮:${本命.身宮地支}`);
  p('│');
  p('├命盤十二宮');
  p('│ │');

  // 依地支序（子起）列十二宮
  const 年支 = 生.年柱.支;
  for (let 支 = 0; 支 < 12; 支++) {
    const i = 支轉宮(支);
    const q = 本命.宮位[i];
    const 標籤 = [];
    if (q.是身宮) 標籤.push('身宮');
    if (q.是來因宮) 標籤.push('來因');
    p(`│ ├${宮名顯示(q.宮名)}宮[${q.天干}${q.地支}]${標籤.map((t) => `[${t}]`).join('')}`);
    p(`│ │ ├主星 : ${q.主星.length ? q.主星.map((s) => 星標(s)).join(',') : '無'}`);
    p(`│ │ ├輔星 : ${q.輔星.length ? q.輔星.map((s) => 星標(s)).join(',') : '無'}`);
    p(`│ │ ├小星 : ${q.雜曜.length ? q.雜曜.map((s) => s.名稱).join(',') : '無'}`);
    p('│ │ ├神煞');
    p(`│ │ │ ├歲前星 : ${q.歲前十二神}`);
    p(`│ │ │ ├將前星 : ${q.將前十二神}`);
    p(`│ │ │ ├十二長生 : ${q.長生十二神}`);
    p(`│ │ │ └博士十二神 : ${q.博士十二神}`);
    p(`│ │ ├大限 : ${q.大限.起}~${q.大限.迄}虛歲`);
    p(`│ │ ├小限 : ${q.小限歲數.slice(0, 5).join(',')}虛歲`);
    const 流年歲 = ((支 - 年支 + 12) % 12) + 1;
    p(`│ │ ├流年 : ${[0, 1, 2, 3, 4].map((k) => 流年歲 + 12 * k).join(',')}虛歲`);
    if (運限) {
      const 層們 = [['大限', 運限.大限], ['小限', 運限.小限], ['流年', 運限.流年], ['流月', 運限.流月], ['流日', 運限.流日], ['流時', 運限.流時]]
        .filter(([名]) => 選層.includes(名));
      const 疊 = 層們.map(([名, 層]) => `${名}${宮名顯示(層.宮名[i])}`);
      p(`│ │ └限流疊宮 : ${疊.join('、') || '無'}`);
    } else {
      p('│ │ └限流疊宮 : 無（未指定運限目標日期）');
    }
    p('│ │');
  }

  // 十二大限與其流年
  p('├十二大限流年');
  p('│ │');
  const { 命宮, 局數, 方向 } = 本命._內部;
  for (let k = 0; k < 12; k++) {
    const i = 宮(命宮 + 方向 * k);
    const q = 本命.宮位[i];
    const 起 = 局數 + k * 10;
    const 流年列 = [];
    for (let a = 起; a <= 起 + 9; a++) {
      const 西元 = 生.農曆.年 + a - 1;
      const 干支 = 曆.年干支(西元);
      流年列.push(`${a}歲${天干[干支.干]}${地支[干支.支]}(${西元})`);
    }
    const 前綴 = k === 11 ? '│ └' : '│ ├';
    p(`${前綴}第${k + 1}大限 ${起}~${起 + 9}虛歲 ${宮名顯示(q.宮名)}宮[${q.天干}${q.地支}]`);
    p(`│ ${k === 11 ? ' ' : '│'} └流年 : ${流年列.join('、')}`);
  }
  p('│');

  // 各運限維度盤（有指定目標日期且有選層時）
  if (運限 && 選層.length) {
    p('├運限信息');
    p(`│ └目標時間 : 國曆 ${運限.目標國曆}（${運限.目標農曆}），虛歲${運限.虛歲}`);
    p('│');
    const 層映 = { 大限: 運限.大限, 小限: 運限.小限, 流年: 運限.流年, 流月: 運限.流月, 流日: 運限.流日, 流時: 運限.流時 };
    for (const 名 of 全部層) {
      if (選層.includes(名)) 層盤段(p, 本命, 名, 層映[名]);
    }
  }

  p('└[備註: 本命盤由 ziwei-chart 排出，宮位以地支標示；亮度為廟旺得利平不陷]');
  return 行.join('\n');
}

module.exports = { 文字盤 };
