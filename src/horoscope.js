'use strict';
/**
 * 運限計算：大限、小限、流年、流月、流日、流時。
 *
 * 各層規則：
 *   - 大限：以虛歲落入各宮十年區間，未及起運歲數則以命宮為童限。
 *   - 小限：依年支三合起宮（寅午戌人起辰…），男順女逆，一歲一宮。
 *   - 流年：以目標農曆年之地支所在宮為流年命宮，四化依流年天干。
 *   - 流月（斗君法）：自流年支宮起正月逆數至生月，再起子時順數至生時為正月斗君，
 *     斗君順行至目標月即流月宮；流月干支以流年干五虎遁、月支建寅。
 *   - 流日：自流月宮起初一順行，每日一宮；干支為當日日柱。
 *   - 流時：自流日宮起子時順行；干支以日干五鼠遁。
 *   - 各層皆有該層四化與十顆流曜（昌曲魁鉞祿羊陀馬鸞喜），流年另有年解與將前／歲前十二神。
 */
const 曆 = require('./calendar');
const C = require('./constants');
const S = require('./stars');

const { 天干, 地支 } = 曆;
const { 支轉宮, 宮轉支, 宮 } = S;

/** 自某宮起逆排十二宮名 */
function 宮名列(命宮位) {
  return Array.from({ length: 12 }, (_, i) => C.宮名序[宮(命宮位 - i)]);
}

function 層資料(宮位, 干, 支, 前綴, 含年解 = false) {
  return {
    宮位索引: 宮(宮位),
    天干: 天干[干],
    地支: 地支[支],
    宮名: 宮名列(宮(宮位)),
    四化: Object.fromEntries(C.四化表[干].map((星, i) => [C.四化名[i], 星])),
    流曜: S.安流曜(干, 支, 前綴, 含年解),
  };
}

/**
 * 排運限
 * @param {object} 本命 排盤() 之結果
 * @param {object} 目標 { 年, 月, 日, 時辰 }（國曆，時辰 0–12，預設 0）
 */
function 排運限(本命, 目標) {
  const { 生, 命宮, 局數, 方向 } = 本命._內部;
  const 時辰 = Number(目標.時辰 ?? 0);
  if (!(時辰 >= 0 && 時辰 <= 12)) throw new RangeError('時辰須為 0–12（12 = 晚子時）');

  // 晚子時：日數值加一（不換月）、日柱採翌日
  const 國曆 = { 年: 目標.年, 月: 目標.月, 日: 目標.日 };
  let 日柱國曆 = 國曆;
  if (時辰 === 12) {
    const d = new Date(Date.UTC(國曆.年, 國曆.月 - 1, 國曆.日) + 86400000);
    日柱國曆 = { 年: d.getUTCFullYear(), 月: d.getUTCMonth() + 1, 日: d.getUTCDate() };
  }
  const 時支 = 時辰 % 12;
  const 農曆 = 曆.國曆轉農曆(國曆.年, 國曆.月, 國曆.日);

  const 虛歲 = 農曆.年 - 生.農曆.年 + 1;
  if (虛歲 < 1) throw new RangeError('目標日期早於出生年');

  // 大限；未達起運歲數為童限：一命二財三疾厄、四歲夫妻五福德、六歲官祿
  let 大限宮, 名稱, 起, 迄;
  if (虛歲 < 局數) {
    const 童限序 = [0, 4, 5, 2, 10, 8]; // 宮名序索引（命宮、財帛、疾厄、夫妻、福德、官祿）
    大限宮 = 宮(命宮 - 童限序[Math.min(虛歲, 6) - 1]);
    名稱 = '童限';
    起 = 1;
    迄 = 局數 - 1;
  } else {
    const 大限序 = Math.floor((虛歲 - 局數) / 10);
    大限宮 = 宮(命宮 + 方向 * 大限序);
    名稱 = '大限';
    起 = 局數 + 大限序 * 10;
    迄 = 局數 + 大限序 * 10 + 9;
  }
  const 大限干 = (本命._內部.寅宮干 + 大限宮) % 10;
  const 大限 = { ...層資料(大限宮, 大限干, 宮轉支(大限宮), '運'), 名稱, 起, 迄 };

  // 小限
  const 小限起 = 支轉宮(C.小限起點[生.年柱.支 % 4]);
  const 小限方向 = 生.性別 === '男' ? 1 : -1;
  const 小限宮 = 宮(小限起 + 小限方向 * (虛歲 - 1));
  const 小限干 = (本命._內部.寅宮干 + 小限宮) % 10;
  const 小限 = { ...層資料(小限宮, 小限干, 宮轉支(小限宮), '小'), 虛歲 };
  delete 小限.流曜; // 小限不另安流曜

  // 流年
  const 年柱 = 曆.年干支(農曆.年);
  const 流年宮 = 支轉宮(年柱.支);
  const 流年 = {
    ...層資料(流年宮, 年柱.干, 年柱.支, '流', true),
    將前十二神: Array.from({ length: 12 }, (_, i) => C.將前十二神[宮(i - 支轉宮([0, 9, 6, 3][年柱.支 % 4]))]),
    歲前十二神: Array.from({ length: 12 }, (_, i) => C.歲前十二神[宮(i - 流年宮)]),
  };

  // 流月（斗君法）：閏月過半（十六起）一律進一月，生月與目標月皆同（不分晚子時）
  const 斗君生月 = 生.農曆.月 + (生.農曆.閏 && 生.農曆.日 > 15 ? 1 : 0);
  const 目標月 = 農曆.月 + (農曆.閏 && 農曆.日 > 15 ? 1 : 0);
  const 斗君 = 宮(流年宮 - (斗君生月 - 1) + 生.時支);
  const 流月宮 = 宮(斗君 + 目標月 - 1);
  const 月柱 = 曆.月干支(年柱.干, 目標月 > 12 ? 目標月 - 12 : 目標月);
  const 流月 = 層資料(流月宮, 月柱.干, 月柱.支, '月');

  // 流日
  const 流日宮 = 宮(流月宮 + 農曆.日 - 1);
  const 日柱序 = 曆.日干支序(日柱國曆.年, 日柱國曆.月, 日柱國曆.日);
  const 流日 = 層資料(流日宮, 日柱序 % 10, 日柱序 % 12, '日');

  // 流時
  const 流時宮 = 宮(流日宮 + 時支);
  const 時柱 = 曆.時干支(日柱序 % 10, 時支);
  const 流時 = 層資料(流時宮, 時柱.干, 時柱.支, '時');

  return {
    目標國曆: `${目標.年}-${目標.月}-${目標.日}`,
    目標農曆: 曆.農曆中文(農曆.年, 農曆.月, 農曆.日, 農曆.閏),
    虛歲,
    大限, 小限, 流年, 流月, 流日, 流時,
    導航: 計算導航(農曆, 國曆, 時辰),
  };
}

/* ── 運限步進導航：計算各維度前後一步的目標日期（農曆語意精準） ── */

const 補零 = (n) => String(n).padStart(2, '0');
const 國曆字串 = (s) => `${s.年}-${補零(s.月)}-${補零(s.日)}`;

/** 農曆月份步進（含閏月序：…四月→閏四月→五月…） */
function 步進農曆月(年, 月, 閏, 方向) {
  if (方向 > 0) {
    if (!閏 && 曆.閏月(年) === 月) return { 年, 月, 閏: true };
    if (月 === 12) return { 年: 年 + 1, 月: 1, 閏: false };
    return { 年, 月: 月 + 1, 閏: false };
  }
  if (閏) return { 年, 月, 閏: false };
  if (月 === 1) return { 年: 年 - 1, 月: 12, 閏: 曆.閏月(年 - 1) === 12 };
  return { 年, 月: 月 - 1, 閏: 曆.閏月(年) === 月 - 1 };
}

/** 農曆日期轉國曆字串，日數超過該月天數時夾至月底 */
function 轉國曆夾日(年, 月, 日, 閏) {
  const 上限 = 閏 ? 曆.閏月天數(年) : 曆.月天數(年, 月);
  const s = 曆.農曆轉國曆(年, 月, Math.min(日, 上限), 閏);
  return 國曆字串(s);
}

function 計算導航(農曆, 國曆, 時辰) {
  const 安全 = (fn) => { try { return fn(); } catch { return null; } };
  const 步年 = (差) => 安全(() => {
    const 年 = 農曆.年 + 差;
    const 閏 = 農曆.閏 && 曆.閏月(年) === 農曆.月;
    return { targetDate: 轉國曆夾日(年, 農曆.月, 農曆.日, 閏), targetTimeIndex: 時辰 };
  });
  const 步月 = (方向) => 安全(() => {
    const m = 步進農曆月(農曆.年, 農曆.月, 農曆.閏, 方向);
    return { targetDate: 轉國曆夾日(m.年, m.月, 農曆.日, m.閏), targetTimeIndex: 時辰 };
  });
  const 步日 = (差) => 安全(() => {
    const d = new Date(Date.UTC(國曆.年, 國曆.月 - 1, 國曆.日) + 差 * 86400000);
    return { targetDate: 國曆字串({ 年: d.getUTCFullYear(), 月: d.getUTCMonth() + 1, 日: d.getUTCDate() }), targetTimeIndex: 時辰 };
  });
  // 流時依時序循環：…亥(11)→晚子(12)→翌日早子(0)→丑(1)…
  const 步時 = (方向) => 安全(() => {
    let t = 時辰 + 方向;
    let 跨日 = 0;
    if (t > 12) { t = 0; 跨日 = 1; }
    if (t < 0) { t = 12; 跨日 = -1; }
    const d = new Date(Date.UTC(國曆.年, 國曆.月 - 1, 國曆.日) + 跨日 * 86400000);
    return { targetDate: 國曆字串({ 年: d.getUTCFullYear(), 月: d.getUTCMonth() + 1, 日: d.getUTCDate() }), targetTimeIndex: t };
  });
  return {
    大限: { 上: 步年(-10), 下: 步年(10) },
    流年: { 上: 步年(-1), 下: 步年(1) },
    流月: { 上: 步月(-1), 下: 步月(1) },
    流日: { 上: 步日(-1), 下: 步日(1) },
    流時: { 上: 步時(-1), 下: 步時(1) },
  };
}

module.exports = { 排運限 };
