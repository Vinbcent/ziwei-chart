const 紀元 = Date.UTC(1900, 0, 31); // 1900-01-31 = 農曆正月初一
const 一日毫秒 = 86400000;
const 紀元日干支 = 40; // 1900-01-31 為甲辰日（六十甲子序 40）

function 年資料(農曆年) {
  if (農曆年 < 1900 || 農曆年 > 2100) throw new RangeError(`農曆年 ${農曆年} 超出支援範圍（1900–2100）`);
  return 農曆資料[農曆年 - 1900];
}

/** 該農曆年之閏月月份（0 = 無閏月） */
function 閏月(農曆年) {
  return 年資料(農曆年) & 0xf;
}

/** 該農曆年閏月之天數（無閏月回傳 0） */
function 閏月天數(農曆年) {
  if (!閏月(農曆年)) return 0;
  return 年資料(農曆年) & 0x10000 ? 30 : 29;
}

/** 該農曆年某月（非閏月）之天數 */
function 月天數(農曆年, 月) {
  return 年資料(農曆年) & (0x8000 >> (月 - 1)) ? 30 : 29;
}

/** 該農曆年總天數 */
function 年天數(農曆年) {
  let 總 = 348; // 12 × 29
  for (let i = 0x8000; i > 0x8; i >>= 1) {
    if (年資料(農曆年) & i) 總 += 1;
  }
  return 總 + 閏月天數(農曆年);
}

/** 自紀元（1900-01-31）起算之日數 */
function 紀元日數(年, 月, 日) {
  return Math.round((Date.UTC(年, 月 - 1, 日) - 紀元) / 一日毫秒);
}

/**
 * 國曆轉農曆
 * @returns {{年:number, 月:number, 日:number, 閏:boolean}}
 */
function 國曆轉農曆(年, 月, 日) {
  let 餘 = 紀元日數(年, 月, 日);
  if (餘 < 0) throw new RangeError('日期早於 1900-01-31，超出支援範圍');
  let 農年 = 1900;
  for (;;) {
    const 天 = 年天數(農年);
    if (餘 < 天) break;
    餘 -= 天;
    農年 += 1;
  }
  const 閏 = 閏月(農年);
  let 農月 = 1;
  let 是閏月 = false;
  for (;;) {
    let 天 = 月天數(農年, 農月);
    if (餘 < 天) break;
    餘 -= 天;
    if (農月 === 閏 && !是閏月) {
      // 該月之後接閏月
      天 = 閏月天數(農年);
      if (餘 < 天) {
        是閏月 = true;
        break;
      }
      餘 -= 天;
    }
    農月 += 1;
  }
  return { 年: 農年, 月: 農月, 日: 餘 + 1, 閏: 是閏月 };
}

/**
 * 農曆轉國曆
 * @returns {{年:number, 月:number, 日:number}}
 */
function 農曆轉國曆(農年, 農月, 農日, 是閏月 = false) {
  if (農月 < 1 || 農月 > 12) throw new RangeError(`農曆月 ${農月} 無效`);
  if (是閏月 && 閏月(農年) !== 農月) throw new RangeError(`農曆 ${農年} 年無閏${農月}月`);
  const 上限 = 是閏月 ? 閏月天數(農年) : 月天數(農年, 農月);
  if (農日 < 1 || 農日 > 上限) throw new RangeError(`農曆 ${農年} 年${是閏月 ? '閏' : ''}${農月}月只有 ${上限} 日`);
  let 位移 = 0;
  for (let y = 1900; y < 農年; y++) 位移 += 年天數(y);
  const 閏 = 閏月(農年);
  for (let m = 1; m < 農月; m++) {
    位移 += 月天數(農年, m);
    if (m === 閏) 位移 += 閏月天數(農年);
  }
  if (是閏月) 位移 += 月天數(農年, 農月);
  位移 += 農日 - 1;
  const d = new Date(紀元 + 位移 * 一日毫秒);
  return { 年: d.getUTCFullYear(), 月: d.getUTCMonth() + 1, 日: d.getUTCDate() };
}

/** 六十甲子序（0=甲子）轉干支字串 */
function 甲子名(序) {
  const n = ((序 % 60) + 60) % 60;
  return 天干[n % 10] + 地支[n % 12];
}

/** 農曆年干支索引 @returns {{干:number, 支:number}} */
function 年干支(農曆年) {
  return { 干: ((農曆年 - 4) % 10 + 10) % 10, 支: ((農曆年 - 4) % 12 + 12) % 12 };
}

/**
 * 月干支（五虎遁）：依年干求各月天干；月支固定建寅（正月=寅）。
 * @param {number} 年干索引
 * @param {number} 農曆月 1–12（閏月以本月計）
 */
function 月干支(年干索引, 農曆月) {
  const 寅月干 = ((年干索引 % 5) * 2 + 2) % 10;
  return { 干: (寅月干 + 農曆月 - 1) % 10, 支: (2 + 農曆月 - 1) % 12 };
}

/** 日干支索引（0=甲子），依國曆日期 */
function 日干支序(年, 月, 日) {
  return ((紀元日數(年, 月, 日) + 紀元日干支) % 60 + 60) % 60;
}

/**
 * 時干支（五鼠遁）：依日干求時干。
 * @param {number} 日干索引
 * @param {number} 時支索引 0=子 … 11=亥
 */
function 時干支(日干索引, 時支索引) {
  return { 干: ((日干索引 % 5) * 2 + 時支索引) % 10, 支: 時支索引 };
}

const 時辰名 = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥'];
const 時辰範圍 = [
  '00:00–01:00', '01:00–03:00', '03:00–05:00', '05:00–07:00', '07:00–09:00', '09:00–11:00',
  '11:00–13:00', '13:00–15:00', '15:00–17:00', '17:00–19:00', '19:00–21:00', '21:00–23:00',
  '23:00–00:00',
];

/** 24 小時制鐘點轉時辰索引（0–12，12 = 晚子時 23:00–00:00） */
function 鐘點轉時辰(小時) {
  if (小時 < 0 || 小時 > 23) throw new RangeError('小時須為 0–23');
  return 小時 === 23 ? 12 : Math.floor((小時 + 1) / 2);
}

const 中文數字 = ['〇', '一', '二', '三', '四', '五', '六', '七', '八', '九', '十'];

function 中文日(日) {
  if (日 === 10) return '初十';
  if (日 === 20) return '二十';
  if (日 === 30) return '三十';
  const 十位 = Math.floor(日 / 10);
  const 個位 = 日 % 10;
  const 前綴 = ['初', '十', '廿'][十位];
  return 前綴 + 中文數字[個位];
}

function 中文月(月) {
  const 名 = ['正', '二', '三', '四', '五', '六', '七', '八', '九', '十', '十一', '十二'];
  return 名[月 - 1] + '月';
}

function 中文年(年) {
  return String(年).split('').map((c) => 中文數字[Number(c)]).join('');
}

/** 農曆日期之中文表示，例：二〇〇〇年閏七月十七 */
function 農曆中文(農年, 農月, 農日, 是閏月 = false) {
  return `${中文年(農年)}年${是閏月 ? '閏' : ''}${中文月(農月)}${中文日(農日)}`;
}

